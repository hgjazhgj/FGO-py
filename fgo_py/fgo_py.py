class Fuse(object):
    def __init__(self, fv=300):
        self.__value = 0
        self.__max = fv

    @property
    def value():
        return self.__value

    def increase(self):
        self.__value += 1
        if self.__value > self.__max:
            printer('Fused')
            beep()
            exit(0)

    def reset(self):
        self.__value = 0
        return True

    def show(self):
        printer(self.__value, '/', self.__max, sep='', flush=True)


